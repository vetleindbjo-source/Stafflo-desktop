type Translations = typeof translations['no']
type TranslationKey = keyof Translations

export function t(lang: Lang, key: TranslationKey, ...args: unknown[]): string {
  const dict = translations[lang] ?? translations['no']
  const val = (dict as Record<string, unknown>)[key] ?? (translations['no'] as Record<string, unknown>)[key]
  if (typeof val === 'function') return (val as (...a: unknown[]) => string)(...args)
  return val as string
}

export function useT(lang: Lang) {
  return (key: TranslationKey, ...args: unknown[]) => t(lang, key, ...args)
}
